package part_01._03_Control_Flow._02_Comparison_Operators;

public class _02_Comparison_Operators {

	public static void main(String[] args) {
		int x = 1;
		int y = 1;
		System.out.println(x == y);
		System.out.println(x != y);
		System.out.println(x > y);
		System.out.println(x >= y);
		System.out.println(x < y);
		System.out.println(x <= y);
	}

}
